package Core_Java;

public class Nested_loop_example {
    public static void main(String[] args) {
        int k=1;
        int n =4;
        for(int i=1;i<=n;i++)
        {
            for(int j=i;j<=n;j++){
                System.out.print(k+" ");
                k++;

            }

            System.out.println( );



        }



    }
}
